export async function seedPersuasionKnowledgeBase(): Promise<void> {
  try {
    const wsList = await db.select({ id: workspaces.id, name: workspaces.name }).from(workspaces);

    for (const ws of wsList) {
      // Check if a Persuasion KB already exists for this workspace
      const [existing] = await db.select({ id: knowledgeBases.id }).from(knowledgeBases)
        .where(and(eq(knowledgeBases.workspace_id, ws.id), eq(knowledgeBases.name, KB_NAME)))
        .limit(1);

      let kbId: string;
      if (existing) {
        kbId = existing.id;
        // Check what docs already present — only insert missing ones
        const existingDocs = await db.select({ title: knowledgeDocuments.title }).from(knowledgeDocuments)
          .where(eq(knowledgeDocuments.knowledge_base_id, kbId));
        const existingTitles = new Set(existingDocs.map(d => d.title));

        for (const doc of DOCS) {
          if (existingTitles.has(doc.title)) continue;
          try {
            await addDocument({
              knowledgeBaseId: kbId,
              workspaceId: ws.id,
              title: doc.title,
              content: doc.content,
              docType: 'persuasion_guide',
              metadata: { source: 'platform_seed', topic: 'sales_persuasion' },
            });
            logger.info({ workspaceId: ws.id, title: doc.title }, 'Persuasion doc added');
          } catch (err) {
            logger.warn({ err, workspaceId: ws.id, title: doc.title }, 'Failed to add persuasion doc');
          }
        }
      } else {
        // Create new KB + all docs
        const [created] = await db.insert(knowledgeBases).values({
          workspace_id: ws.id,
          name: KB_NAME,
          description: KB_DESCRIPTION,
        }).returning();
        if (!created) continue;
        kbId = created.id;
        logger.info({ workspaceId: ws.id, kbId }, 'Persuasion KB created');

        for (const doc of DOCS) {
          try {
            await addDocument({
              knowledgeBaseId: kbId,
              workspaceId: ws.id,
              title: doc.title,
              content: doc.content,
              docType: 'persuasion_guide',
              metadata: { source: 'platform_seed', topic: 'sales_persuasion' },
            });
            logger.info({ workspaceId: ws.id, title: doc.title }, 'Persuasion doc added');
          } catch (err) {
            logger.warn({ err, workspaceId: ws.id, title: doc.title }, 'Failed to add persuasion doc');
          }
        }
      }
    }
    logger.info('Persuasion KB seeding complete');
  } catch (err) {
    logger.error({ err }, 'Persuasion KB seeding failed (non-fatal)');
  }
}
