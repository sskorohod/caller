/* ─── Build bottom tabs based on workspace plan ──────────────────────── */
export function getBottomTabs(plan: string): TabItem[] {
  const isTranslator = plan === 'translator';
  return [
    { key: 'nav.overview', href: '/dashboard', icon: tabIcons.home },
    { key: 'nav.calls', href: '/dashboard/calls', icon: tabIcons.calls },
    { key: 'nav.dialer', href: '/dashboard/dialer', icon: tabIcons.dialer, accent: true },
    isTranslator
      ? { key: 'nav.translator', href: '/dashboard/translator', icon: tabIcons.translator }
      : { key: 'nav.agents', href: '/dashboard/agents', icon: tabIcons.agents },
    { key: '__more__', href: '#', icon: tabIcons.more },
  ];
}

/* ─── "More" sheet items based on plan ───────────────────────────────── */
export function getMoreItems(plan: string): MoreItem[] {
  const isTranslator = plan === 'translator';

  const sections: MoreItem[] = [];

  if (!isTranslator) {
    sections.push({
      section: 'nav.sectionOperations',
      items: [
        navItems.find(i => i.key === 'nav.translator')!,
        navItems.find(i => i.key === 'nav.missions')!,
      ],
    });
    sections.push({
      section: 'nav.sectionAi',
      items: [
        navItems.find(i => i.key === 'nav.knowledge')!,
        navItems.find(i => i.key === 'nav.prompts')!,
        navItems.find(i => i.key === 'nav.skills')!,
      ],
    });
    sections.push({
      section: 'nav.sectionIntegrations',
      items: [navItems.find(i => i.key === 'nav.connectors')!],
    });
  } else {
    // Translator-only: show translator in "More" since agents tab took its place
    // Actually translator is the 4th tab, so no need
  }

  sections.push({
    section: 'nav.sectionSystem',
    items: [
      ...(!isTranslator ? [navItems.find(i => i.key === 'nav.audit')!] : []),
      navItems.find(i => i.key === 'nav.billing')!,
      navItems.find(i => i.key === 'nav.settings')!,
    ],
  });

  return sections;
}
